// + - * / plus, minus, times and divide operators

console.log(10 / 5); // logs 2

console.log(10 + 5); // logs 15
// + can also be used to concatinate strings ( add strings together!)
console.log("Hello" + "World!"); // logs 'HelloWorld!' as one word

// === checks for equality
console.log(10 === 5); // logs false as 10 is not equal to 5
console.log(10 === 10); // logs true

// > checks for greater than
// < checks for less than
// >= checks for greater than or equal to
// <= checks for less than or equal to
if (10 >= 10) {
  console.log("works"); // logs works
}
